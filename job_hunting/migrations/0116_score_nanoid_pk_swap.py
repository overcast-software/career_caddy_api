"""CC-77 #79 — Score integer PK -> 10-char NanoID PK (true PK swap).

Score is a **leaf**: no foreign key anywhere references ``score(id)``, so
this is the simplest shape of the swap proven in
``0114_skill_nanoid_pk_swap`` — just the parent PK is repointed, with no
dependent FK columns to rebuild.

Score's two composite UNIQUE constraints (``unique_score_per_job_resume_user``
and the partial ``unique_score_per_job_user_career_data``) ride on
``job_post_id`` / ``resume_id`` / ``user_id`` — NOT on ``score.id`` — so the
PK swap leaves them untouched.

DB surgery is hand-written ``RunSQL`` (a PK-type change is not cleanly
auto-generated); Django's migration *state* is kept in sync with a single
``AlterField`` on ``score.id`` via ``SeparateDatabaseAndState``.

## Reverse path (down migration)

Reversing a destructive PK swap cannot restore the original integer ids.
The reverse re-mints a FRESH sequential integer PK (deterministic
``row_number`` order) — a structurally-valid integer-PK schema, NOT a
value-preserving round-trip.
"""

from __future__ import annotations

