logger = logging.getLogger(__name__)


def backfill_nanoids(apps, schema_editor):
    """Mint a unique NanoID for every ``score`` row."""
    from job_hunting.models.nanoid_pk import generate_nanoid

    with schema_editor.connection.cursor() as cur:
        cur.execute("SELECT id FROM score ORDER BY id")
        score_ids = [row[0] for row in cur.fetchall()]

        used: set[str] = set()
        for old_id in score_ids:
            nid = generate_nanoid()
            while nid in used:  # astronomically rare; keep it deterministic
                nid = generate_nanoid()
            used.add(nid)
            cur.execute(
                "UPDATE score SET new_id = %s WHERE id = %s", [nid, old_id]
            )

    logger.info("0116 nanoid backfill: minted %s score ids.", len(score_ids))


